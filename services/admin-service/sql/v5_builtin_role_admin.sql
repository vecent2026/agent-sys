-- admin_system 内置角色与 admin 账号保护 v5
-- 修复内容：
--   1. tenant_role 表新增 is_builtin 字段，标记内置不可删改的角色
--   2. 将 id=1 的超级管理员角色标记为内置
--   3. 确保 platform_user 中 admin 账号 is_super=1
--   4. 确保 admin 账号与超管角色已关联
-- ============================================================

USE admin_system;
SET NAMES utf8mb4;

-- ------------------------------------------------------------
-- Step 1: tenant_role 新增 is_builtin 字段
-- ------------------------------------------------------------
ALTER TABLE `tenant_role`
  ADD COLUMN `is_builtin` TINYINT(1) NOT NULL DEFAULT 0 COMMENT '是否内置角色(1=是，不可删除/编辑；0=否)';

-- ------------------------------------------------------------
-- Step 2: 将超级管理员角色（id=1）标记为内置
-- ------------------------------------------------------------
UPDATE `tenant_role` SET `is_builtin` = 1 WHERE `id` = 1;

-- ------------------------------------------------------------
-- Step 3: 确保 admin 账号 is_super=1（超管标志）
-- ------------------------------------------------------------
UPDATE `platform_user` SET `is_super` = 1 WHERE `username` = 'admin';

-- ------------------------------------------------------------
-- Step 4: 确保 admin 账号已关联到超管角色（id=1）
--         使用 INSERT IGNORE 防止重复插入
-- ------------------------------------------------------------
INSERT IGNORE INTO `platform_user_role` (`user_id`, `role_id`)
SELECT u.`id`, 1
FROM `platform_user` u
WHERE u.`username` = 'admin';

